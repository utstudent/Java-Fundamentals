package labs_examples.objects_classes_methods.labs.methods;

public class Person {
    int age;
    String name;

    public Person(int age, String name) {
        this.age = age;
        this.name = name;
    }

    @Override
    public String toString() {
        return "Person{" +
                "age=" + age +
                ", name='" + name + '\'' +
                '}';
    }

    public void ageUp() {
        this.age = this.age + 1;
    }
}
